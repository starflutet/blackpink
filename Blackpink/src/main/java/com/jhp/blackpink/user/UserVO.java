package com.jhp.blackpink.user;

public class UserVO {
	private int i_user;
	private String uid;
	private String upw;
	private String nm;
	private String s_pk;
	private String r_dt;
	
	public int getI_user() {
		return i_user;
	}
	public void setI_user(int i_user) {
		this.i_user = i_user;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getUpw() {
		return upw;
	}
	public void setUpw(String upw) {
		this.upw = upw;
	}
	public String getNm() {
		return nm;
	}
	public void setNm(String nm) {
		this.nm = nm;
	}
	public String getS_pk() {
		return s_pk;
	}
	public void setS_pk(String s_pk) {
		this.s_pk = s_pk;
	}
	public String getR_dt() {
		return r_dt;
	}
	public void setR_dt(String r_dt) {
		this.r_dt = r_dt;
	}
}
