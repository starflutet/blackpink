public class NoticeVO {
	private int i_notice;
	private int i_user;
	private String nm;
	private String title;
	private String noticetnt;
	private String r_dt;
	
	public int getI_notice() {
		return i_notice;
	}
	public void setI_notice(int i_notice) {
		this.i_notice = i_notice;
	}
	public int getI_user() {
		return i_user;
	}
	public void setI_user(int i_user) {
		this.i_user = i_user;
	}
	public String getNm() {
		return nm;
	}
	public void setNm(String nm) {
		this.nm = nm;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getNoticetnt() {
		return noticetnt;
	}
	public void setNoticetnt(String noticetnt) {
		this.noticetnt = noticetnt;
	}
	public String getR_dt() {
		return r_dt;
	}
	public void setR_dt(String r_dt) {
		this.r_dt = r_dt;
	}
}
